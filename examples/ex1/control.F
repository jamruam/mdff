!#Example 1: LJ fcc structure at low temperature

! ======================================================================
! main parameters
! ======================================================================
&controltag
        calc          = 'md'      ! md calculation
&end
! ======================================================================
! parameters for the molecular dynamics 
! ======================================================================
&mdtag
        integrator    = 'nve-vv'  
        npas          = 1000      ! md steps
        dt            = 0.001     ! time step 
        temp          = 0.8       ! temperature  
        nprint        = 100       ! printing info each nprint
        fprint        = 1         ! printing info to OSZIFF
        nequil        = 10        ! equilibration for nequil steps
        nequil_period = 1         ! period of rescaling 
        ltraj         = .false.   
&end
! ======================================================================
! parameters for the force-field 
! ======================================================================
&fieldtag
        epslj(1,1)         = 1.0d0     ! lennard-jones potential
        sigmalj(1,1)      = 1.0d0     ! lennard-jones potential
&end
! ======================================================================
! parameters for the configuration (only really used if the configuration
! is generated by the code. 
! ======================================================================
&configtag
        lgenconf      = .false.   ! reading configuration in POSFF 
&end
! ======================================================================
! no properties yet
! ======================================================================
&proptag 
&end
